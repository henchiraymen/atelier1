        parent::__construct($registry, Author::class);
    }

    public function listAuthorByEmailQB(){
        $req = $this->createQueryBuilder('a')
        ->orderBy('a.email','ASC')
        ->getQuery();
        return $req->getResult();
    }
    function listAuthorByEmailDQL(){
        $em=$this->getEntityManager();
        $req=$em->createQuery('select a from App\Entity\Author a ORDER BY a.email DESC');
        return $req->getResult();
    }
    public function searchAuth($nom){
        $req = $this->createQueryBuilder('a')
        ->where('a.username like :n')
        ->setParameter(':n', '%'.$nom.'%')
        ->getQuery();
        return $req->getResult();
    }

